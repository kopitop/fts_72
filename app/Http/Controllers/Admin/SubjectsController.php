    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        try {
             $this->subjectRepository->delete($id);
        } catch (\Exception $e) {

            return redirect()->action('Admin\SubjectsController@index')
